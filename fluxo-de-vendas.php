                    <p>
                        O que é fluxo de cadência?
                    </p>
                    <p>
                        Na reunião de kickoff, a grande maioria dos mais dos clientes chegam querendo saber qual é o fluxo de cadência para prospecção ideal, porém as respostas são sempre as mesmas, não existe somente fluxo de cadência para prospecção.
                    </p>
                    <p>
                        Pode, e deve, estruturar fluxos a serem utilizados ao longo de todo o funil de vendas. Para obter o fluxo com maior taxa de conversão no funil de vendas precisa ser definido o ICP e como ele se comporta nas tentativas de contato.
                    </p>
                    <p>
                        Um erro geral dos clientes que estão começando a se aprofundar nos novos processos de vendas é consumir vários materiais educativos que estão disponíveis online.
                    </p>
                    <p>
                        Não que isso seja péssimo, mas é cultural do brasileiro querer copiar e aplicar logo o conhecimento que adquiriu e se esquecer dos fundamentos básicos por trás daquilo.
                    </p>
                    <p>
                        Para obter sucesso é necessário entender os fundamentos básicos. O primeiro ponto para se ter um bom fluxo de cadência é entender que é o objetivo dele.
                    </p>
                    <p>
                        <b><i>" Fluxos de cadência, ou cadência em vendas, é um plano sistematizado de uma série de interações que usam diferentes métodos de comunicação para aumentar a chance de contato com um prospect."</i></b>
                    </p>
                    <p class="text-center">
                        <img src="img/vendas/fluxo-de-comunicacao.jpg" width="100%">

            <h2><b>Descubra como ter um dream team.</b></h2>
                <div class="text-justify">
                    <p>Quem nunca sonhou em trabalhar em uma equipe de alta performance?</p>
                    <p>
                        Todo gestor possui dificuldades na direção de um time, sendo o trabalho em equipe um dos principais desafios no seu dia a dia. E tal tarefa desencadeia inúmeros problemas, como atingimento de metas, entrega nos prazos corretos, qualidade dos projetos e satisfação dos stakeholders.
                    </p>
                    <p>
                        A grande questão é equilibrar a balança para atingir excelentes resultados usufruindo da melhor forma as pessoas do seu time. Afinal, de que adianta ter bons produtos, se o meu time não consegue fazer a máquina girar em sua plena performance?
                    </p>
                    <p>
                        Atingir o flow da alta performance vai além de conseguir ter as pessoas certas nos lugares certos, pois é necessário o comportamento humano em um ambiente corporativo e em atividades em grupo.
                    </p>
                    <p>
                        Dessa forma, analisando experimentos corporativos e cases, conseguimos afirmar que equipes altamente eficazes, conseguem entender de fato o conceito do que é um time. E o norte do time, guiado pelo líder, parte de alguns princípios importantes compartilhados no livro Os 5 Desafios das Equipes de Patrick Lencioni.
                        Caso você seja um gestor de equipe, o livro contribuirá com informações que levará seu time a um rumo mais funcional e gerador de resultados. O autor passa informações sobre os 5 desafios que uma equipe deve superar para ser bem-sucedida, de forma objetiva, leve e motivadora, pois vai transmitindo os conceitos por meio de uma história fictícia de uma empresa, que está com problemas.
                        Por fim, o Patrick Lencioni apresenta detalhadamente as cinco disfunções das equipes, como podem ser superadas e além disso, considera um questionário para facilitar o diagnóstico da suscetibilidade das equipes às 5 disfunções.
                    </p>
                    <p>
                        Abaixo segue a definição breve de cada desafio:
                    </p>
                    <p class="text-center">
                        <img src="img/gestao/piramide.png">
                    </p>
                    <p>
                        <h3><b>1º Falta de confiança:</b></h3>
                    </p>
                    <p>
                        "A falta de confiança não permite que as relações dentro da equipe sejam verdadeiras e abertas a ponto de que os seus membros possam demonstrar vulnerabilidade, ou seja possam errar, admitir seus erros e seguir em frente."
                    </p>
                    <p>
                        <h3><b>2º Medo do conflito:</b></h3>
                    </p>
                    <p>
                        "Diz respeito à falta de capacidade dos membros da equipe para se envolverem num forte debate de ideias sem censura. Discordar até que todos possam manifestar suas ideias completamente e a partir disso construir novas possibilidades."
                    </p>
                    <p>
                        <h3><b>3º Falta de comprometimento:</b></h3>
                    </p>
                    <p>
                        "Sem discutir ideias abertamente fica difícil se comprometer com decisões e objetivos com os quais talvez não haja concordância. O pior é que às vezes os membros da equipe fingem se comprometer, mas na realidade não agem em favor dos combinados."
                    </p>
                    <p>
                        <h3><b>4º Evitar responsabilidade:</b></h3>
                    </p>
                    <p>
                        "Assumir responsabilidades e chamar a atenção dos demais envolvidos em relação a comportamentos e atitudes é mais difícil quando não estamos comprometidos e não confiamos nos demais membros da equipe."
                    </p>
                    <p>
                        <h3><b>5º Falta de atenção aos resultados:</b></h3>
                    </p>
                    <p>
                        "Quando os membros da equipe colocam em primeiro lugar seus interesses individuais ou de suas áreas, em detrimento dos objetivos coletivos da empresa, os resultados correm grande risco."
                    </p>
                    <p>
                        Contudo, é importante entender e se conscientizar que tais disfunções podem resultar em problemas mais complexos dentro das equipes e atingir diretamente os resultados esperados,  causando impacto negativo a empresa. Por isso, é válido a leitura do livro para que possa levantar como trabalhar os desafios e adaptar as soluções a sua equipe, alcançando assim melhores performances em grupo.
                    </p>
